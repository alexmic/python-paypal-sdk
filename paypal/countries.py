def get_ordered():
    """Retrieve a list of all contries in their intended order."""
    global _ordered_cache
    cache = _ordered_cache.get('countries', None)
    if cache is not None:
        return cache

    ordered = [COUNTRIES[cc] for cc in ORDER]
    _ordered_cache['countries'] = ordered
    return ordered


